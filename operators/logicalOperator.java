

public class logicalOperator {
    public static void main(String args[]){
        int s=10;
        int d=20;
        boolean a=true;
        System.out.println(s<d && s<d);
        System.out.println(s>d && s>d);
        System.out.println(s<d || s>d);
        System.out.println(!a);
        System.out.println(a);
    }
    
}
