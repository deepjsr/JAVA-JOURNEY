
public class areithmeticop {
    public static void main(String[] args){
        int s=7;
        int d=4;
        int sum=s+d;
        System.out.println(sum);//s+d=9
        System.out.println(s+d);//9
        System.out.println(s/d);//divide=1
        System.out.println(s*d);//multilication=20
        System.out.println(s%d);//modulus=5
    }

}
